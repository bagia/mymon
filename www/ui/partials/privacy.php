    <h3>Privacy policy</h3>
    <h4>Information we receive and use</h4>
    <p>The Facebook authentication is done directly on Facebook, not on our site.
        Plus the information received from the authentication is only stored on the
        client side, except when you start creating Watchdogs.</p>
    <p>After authenticating to Facebook you will see your name and profile picture
        displayed on the site. We do not store them.</p>
    <h4>Information we store</h4>
    <p>All the watchdogs you create are associated with your Facebook id which is a unique
        identifier that allows to namely identify you, and sometimes the article id that
        allows to identify the specific publication that was made.</p>
    <p>If you decide to create a <i>Power Watchdog</i>, then  we will have to store all of
        your Facebook friends names. We won't make them public nor share them with anyone.</p>
    <p>Other information stored is the date of creation of a watchdog, the history of the hits
        of a watchdog, and whether or not a power watchdog is being deployed or deleted on your
        profile.</p>
