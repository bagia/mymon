    <h3>Privacy policy</h3>
    <h4>Information we receive and use</h4>
    <p>The Facebook authentication is done directly on Facebook, not on our site.
        Plus the information received from the authentication is only stored on the
        client side, except when you start creating Watchdogs.</p>
    <p>After authenticating to Facebook you will see your name and profile picture
        displayed on the site. We do not store them.</p>
    <h4>Information we store</h4>
    <p>All the watchdogs you create are associated with your Facebook <i>third party
       id</i> which is an identifier that does not let us identify you namely.</p>
    <p>If you decide to activate <i>notifications</i> on any of your watchdogs, then
        we will have to store your Facebook user id - that permits identification
        by name.</p>
    <p>No other information is stored from your Facebook account.</p>
