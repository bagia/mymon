    <h4>The <i>My Monitor</i> project</h4>
    <p>December 2013</p>
    <p>Since Facebook's early ages, numerous <i>so-called</i> applications have been claiming to be able to reveal to their users
    the identity of the people visiting their profile page. Up to now all these applications can be classified in two categories:</p>
    <ol>
        <li>applications taking advantage of unwary users to publish spam</li>
        <li>applications cross-referencing friends lists and using browser extensions that can only detect users also using the
            same application</li>
    </ol>

    <p>None of the applications that reached the public up to now was a genuine, honest, trustworthy application. That said, the
    enthusiasm for these decoys shows that there is a huge demand from the users and a tremendous lack of supply.</p>

    <p>Shimi Cohenm, in his video <a href="http://www.shimicohen.com/The-Innovation-of-Loneliness" target="_blank">Innovation
    in loneliness</a>, insists ont he importance that one gives to their profile page on a social network: it reflects an
    image of ourselves on which we have total control, an image which is therefore biased but idealised. We invest a lot of time
    and energy in the organization of our online presence in order to create <a href="http://en.wikipedia.org/wiki/Personal_branding"
                                                                                target="_blank">our very own <i>self-brand</i></a>.
    The bottom line is that all this work has one underlying goal: to be seen.</p>

    <p>Facebook, for example, has always declined to its member the list of the users visiting their profile page. The first reason
    that comes to mind is the following: if one day one of your friend suddenly became aware that you are stalking them daily on their
    page, then the coffee break the next morning could quickly get awkward. In other words, if Facebook would reveal who is visiting
    who, then the traffic would fall dramatically because people would not be able to stalk each other in total discretion.</p>

    <p>As probable this hypothesis can seem, our vision of the network's motivations changed over the project development process.
    We agree on the premise that if visits become public then the traffic will fall, however we think the reason that will make
    people desert the social network is not privacy. <b>We think that the statistics will be disappointing.</b> We think the figures
    are not made public because they are way much lower than what people are fantasizing about. So low that people would soon realize
    that no-one is interested in what they have to say. Spending hours selecting the right picture, controlling what people are
    publishing about us, choosing the right formulation just to realize that there is no audience? What for?</p>

    <h5>Genesis of the project</h5>
    <p>This project was inspired by the discovery of a method allowing to create counters, that we call here <b>watchdogs</b>.
    The technical details will follow later on, the main idea being that this method lets a user put a Facebook publication
    in such a state that - <u>almost</u> - each time the publication gets displayed in a browser or a mobile app it is possible
    to increment a counter. This method is not exempt from a quantity of constraints and imprecision but seems sufficiently
    exploitable to validate the aforementioned hypothesis.</p>
